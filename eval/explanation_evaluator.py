import json
import os
import sys

from src.models.explanation import AlertExplanationRequest
from src.services.llm.explanation_service import generate_explanation

def evaluate():
    test_cases_path = os.path.join(os.path.dirname(__file__), "test_cases.json")
    with open(test_cases_path, "r", encoding="utf-8") as f:
        test_cases = json.load(f)

    print(f"Starting evaluation of {len(test_cases)} test cases...")
    
    passed = 0
    failed = 0
    
    for tc in test_cases:
        print(f"\n--- Running: {tc['id']} - {tc['description']} ---")
        req = AlertExplanationRequest(**tc["request"])
        
        try:
            res = generate_explanation(req)
            
            # Deterministic checks
            assert res.alert_id == req.alert_id, "Alert ID mismatch"
            assert res.risk_score == req.risk_score, "Risk score mismatch"
            assert res.severity == req.severity, "Severity mismatch"
            
            # Check JSON validation inherently passed because generate_explanation returns the Pydantic model
            
            # Check missing evidence logic (e.g. tc8)
            if tc["id"] == "tc8":
                assert res.confidence < 0.6, "Confidence too high for missing evidence"
                
            print(f"[{tc['id']}] PASS. Generated by: {res.generated_by}. Confidence: {res.confidence}")
            passed += 1
            
        except Exception as e:
            print(f"[{tc['id']}] FAIL. Error: {e}")
            failed += 1
            
    print("\n--- EVALUATION SUMMARY ---")
    print(f"Total: {len(test_cases)}")
    print(f"Passed: {passed}")
    print(f"Failed: {failed}")
    
    if failed > 0:
        sys.exit(1)

if __name__ == "__main__":
    # Ensure src is in python path
    sys.path.append(os.path.abspath(os.path.join(os.path.dirname(__file__), '..')))
    evaluate()
