def list_event_logs_for_user(
    user_id: str, since: str | None = None, limit: int = 1000
) -> list[dict[str, Any]]:
    """Fetch recent event_logs for a user, optionally filtered by timestamp >= since.

    Used by the user scoring service to assemble the feature window.
    """
    where = ["user_id = %s"]
    params: list[Any] = [user_id]
    if since:
        where.append("timestamp >= %s")
        params.append(since)
    where.append("timestamp IS NOT NULL")
    sql = (
        "SELECT * FROM event_logs WHERE "
        + " AND ".join(where)
        + " ORDER BY timestamp ASC, id ASC LIMIT %s"
    )
    params.append(limit)
    with get_connection() as conn:
        rows = conn.execute(sql, params).fetchall()
    return [_decode_json_fields(_row_to_dict(row) or {}) for row in rows]


