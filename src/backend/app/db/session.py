def retrieve_memories(
    user_id: str | None,
    device_id: str | None,
    factor_tags: list[str] | None,
    top_k: int = 5,
    decay_days: int = 90,
) -> list[dict[str, Any]]:
    """v1 tag-based retrieval. Single query, no N+1.

    Matches: (scope=user AND id=user) OR (scope=device AND id=device)
          OR (scope=pattern AND tags overlap) OR (scope=global).
    Excludes memories not used in the last `decay_days` days.
    """
    user_id = user_id or ""
    device_id = device_id or ""
    factor_tags = factor_tags or []
    with get_connection() as conn:
        rows = conn.execute(
            """
            SELECT * FROM llm_memories
            WHERE (
              (scope = 'user'   AND scope_id = %s AND %s <> '') OR
              (scope = 'device' AND scope_id = %s AND %s <> '') OR
              (scope = 'pattern' AND cardinality(tags) > 0 AND tags && %s) OR
              (scope = 'global')
            )
            AND (last_used_at IS NULL OR last_used_at > NOW() - (%s || ' days')::INTERVAL)
            ORDER BY use_count DESC, last_used_at DESC NULLS LAST
            LIMIT %s
            """,
            (user_id, user_id, device_id, device_id, factor_tags, str(decay_days), top_k),
        ).fetchall()
    return [_row_to_dict(r) for r in rows]


def touch_memories(memory_ids: list[int]) -> None:
    if not memory_ids:
        return
    with get_connection(write=True) as conn:
        conn.execute(
            "UPDATE llm_memories SET use_count = use_count + 1, last_used_at = NOW() "
            "WHERE id = ANY(%s)",
            (memory_ids,),
        )


def forget_memory(memory_id: int) -> None:
    with get_connection(write=True) as conn:
        conn.execute("DELETE FROM llm_memories WHERE id = %s", (memory_id,))


def list_memories_admin(
    scope: str | None = None,
    kind: str | None = None,
    tag: str | None = None,
    limit: int = 100,
) -> list[dict[str, Any]]:
    where_parts: list[str] = []
