@contextmanager
def get_connection(*, write: bool = False, long_running: bool = False) -> Iterable[Any]:
    """Checkout a pooled connection. Backward-compatible signature.

    Pool is initialised in `main.py` lifespan via `init_pool()`. This helper
    applies the per-checkout `row_factory=dict_row` (so every existing
    helper in this module keeps working) and rolls back on exception.

    Args:
        write: set True for INSERT/UPDATE/DELETE — applies write statement_timeout.
        long_running: set True for SSE streams — disables statement_timeout.
    """
    try:
        import psycopg
        from psycopg.rows import dict_row
    except ImportError as exc:
        raise RuntimeError(
            "PostgreSQL driver missing. Install dependencies with `pip install -r requirements.txt`."
        ) from exc

    with pooled_connection(write=write, long_running=long_running) as raw_conn:
        raw_conn.row_factory = dict_row
        try:
            yield raw_conn
            raw_conn.commit()
        except Exception:
            raw_conn.rollback()
            raise


