def _relative_time(value: Any) -> str:
    parsed = _parse_timestamp(value)
    if not parsed:
        return "Chưa rõ"
    return parsed.strftime("%Y-%m-%d %H:%M")

def _time_label(value: Any) -> str:
    parsed = _parse_timestamp(value)
    if not parsed:
        return "--:--"
    return parsed.strftime("%H:%M")

def _parse_timestamp(value: Any) -> datetime | None:
    if value is None:
        return None
    text = str(value).strip()
    for fmt in (None, "%m/%d/%Y %H:%M:%S", "%Y-%m-%d %H:%M:%S"):
        try:
            if fmt is None:
                return datetime.fromisoformat(text.replace("Z", "+00:00"))
            return datetime.strptime(text, fmt)
        except ValueError:
            continue
    return None

def _event_title(event_type: Any, action: Any) -> str:
    event = str(event_type or "event").lower()
    action_text = str(action or "").lower()
    if event == "logon":
        return "Đăng nhập bất thường" if "logon" in action_text else "Logon event"
    if event == "http":
        return "HTTP access được ghi nhận"
    if event == "email":
        return "Email activity"
    if event == "file":
        return "File access"
    if event == "device":
        return "Device activity"
    return event.upper()

def _mitre_label(title: str) -> str:
    lowered = title.lower()
    if "logon" in lowered or "đăng nhập" in lowered:
        return "T1078 - Valid Accounts"
    if "http" in lowered or "web" in lowered:
        return "T1071 - Application Layer Protocol"
    if "file" in lowered or "data" in lowered:
        return "T1005 - Data from Local System"
    return "UEBA - Behavioral Anomaly"


