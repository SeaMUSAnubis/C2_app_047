def _create_llm_schema(conn: Any) -> None:
    """Create tables, indexes, triggers, and autovacuum config for the LLM
    long-term-memory + chat features.

    Idempotent — safe to run on every startup.
    """
    # 1. llm_conversations — multiple analyst threads per alert.
    # Note: `user_id` is the analyst (app_account) holding the conversation,
    # NOT the employee the alert is about. We store it as plain TEXT (no FK)
    # because analysts don't have a row in `users` (which holds employees only).
    conn.execute(
        """
        CREATE TABLE IF NOT EXISTS llm_conversations (
            id BIGINT GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
            alert_id INTEGER NOT NULL REFERENCES alerts(id) ON DELETE CASCADE,
            user_id TEXT NOT NULL,
            title TEXT NOT NULL,
            summary TEXT,
            summarized_through_msg_id BIGINT,
            created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
            updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
        )
        """
    )
    conn.execute("ALTER TABLE llm_conversations DROP CONSTRAINT IF EXISTS llm_conversations_alert_id_key")
    # Migration for tables created with the old FK constraint.
    conn.execute(
        """
        DO $$
        BEGIN
          IF EXISTS (
            SELECT 1 FROM pg_constraint
            WHERE conname = 'llm_conversations_user_id_fkey'
          ) THEN
            ALTER TABLE llm_conversations DROP CONSTRAINT llm_conversations_user_id_fkey;
          END IF;
        END $$
        """
    )
    conn.execute(
        "CREATE INDEX IF NOT EXISTS idx_llm_conv_user_updated "
        "ON llm_conversations (user_id, updated_at DESC)"
    )
    conn.execute(
        "CREATE INDEX IF NOT EXISTS idx_llm_conv_alert_updated "
        "ON llm_conversations (alert_id, updated_at DESC)"
    )
    conn.execute(
        "CREATE INDEX IF NOT EXISTS idx_llm_conv_updated "
        "ON llm_conversations (updated_at DESC)"
    )
    # updated_at touch trigger (shared function — created idempotently).
    conn.execute(
        """
        CREATE OR REPLACE FUNCTION touch_updated_at() RETURNS TRIGGER AS $$
        BEGIN NEW.updated_at = NOW(); RETURN NEW; END;
        $$ LANGUAGE plpgsql
        """
    )
    conn.execute(
        """
        DO $$
        BEGIN
          IF NOT EXISTS (SELECT 1 FROM pg_trigger WHERE tgname = 'trg_llm_conv_updated') THEN
            CREATE TRIGGER trg_llm_conv_updated
              BEFORE UPDATE ON llm_conversations
              FOR EACH ROW EXECUTE FUNCTION touch_updated_at();
          END IF;
        END $$
        """
    )

    # 2. llm_messages — every turn of every chat. High-write.
    conn.execute(
        """
        CREATE TABLE IF NOT EXISTS llm_messages (
            id BIGINT GENERATED ALWAYS AS IDENTITY,
            conversation_id BIGINT NOT NULL REFERENCES llm_conversations(id) ON DELETE CASCADE,
            role TEXT NOT NULL CHECK (role IN ('user', 'assistant', 'system')),
            content TEXT NOT NULL,
            tokens_in INTEGER,
            tokens_out INTEGER,
            model TEXT,
            latency_ms INTEGER,
            memory_used_ids BIGINT[],
            feedback_used_ids BIGINT[],
            created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
            PRIMARY KEY (id, created_at)
        )
        """
    )
    conn.execute(
        "CREATE INDEX IF NOT EXISTS idx_llm_msg_conv_created "
        "ON llm_messages (conversation_id, created_at DESC)"
    )
    conn.execute(
        "CREATE INDEX IF NOT EXISTS idx_llm_msg_model_created "
        "ON llm_messages (model, created_at DESC) WHERE model IS NOT NULL"
    )
    # Autovacuum tuning (high-write table).
    conn.execute(
        "ALTER TABLE llm_messages SET ("
        "autovacuum_vacuum_scale_factor = 0.05, "
        "autovacuum_analyze_scale_factor = 0.02, "
        "autovacuum_vacuum_cost_limit = 1000)"
    )

    # 3. llm_feedback — analyst verdicts.
    conn.execute(
        """
        CREATE TABLE IF NOT EXISTS llm_feedback (
            id BIGINT GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
            alert_id INTEGER NOT NULL REFERENCES alerts(id) ON DELETE CASCADE,
            analyst_id TEXT NOT NULL REFERENCES users(id),
            verdict TEXT NOT NULL CHECK (
                verdict IN ('true_positive', 'false_positive', 'benign', 'needs_investigation')
            ),
            note TEXT,
            created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
            UNIQUE (alert_id, analyst_id)
        )
        """
    )
    conn.execute(
        "CREATE INDEX IF NOT EXISTS idx_llm_feedback_analyst_created "
        "ON llm_feedback (analyst_id, created_at DESC)"
    )
    conn.execute(
        "CREATE INDEX IF NOT EXISTS idx_llm_feedback_alert_verdict "
        "ON llm_feedback (alert_id, verdict)"
    )
    conn.execute(
        "ALTER TABLE llm_feedback SET (autovacuum_vacuum_scale_factor = 0.1)"
    )

    # 4. llm_memories — long-term memory entries.
    conn.execute(
        """
        CREATE TABLE IF NOT EXISTS llm_memories (
            id BIGINT GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
            scope TEXT NOT NULL CHECK (scope IN ('user', 'device', 'pattern', 'global')),
            scope_id TEXT,
            kind TEXT NOT NULL CHECK (kind IN ('fact', 'preference', 'analyst_pattern', 'historical')),
            content TEXT NOT NULL,
            content_hash TEXT NOT NULL,
            tags TEXT[] NOT NULL DEFAULT '{}',
            use_count INTEGER NOT NULL DEFAULT 0,
            last_used_at TIMESTAMPTZ,
            created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
            created_by TEXT REFERENCES users(id),
            UNIQUE (scope, scope_id, kind, content_hash)
        )
        """
    )
    conn.execute(
        "CREATE INDEX IF NOT EXISTS idx_llm_mem_scope_lookup "
        "ON llm_memories (scope, scope_id, use_count DESC, last_used_at DESC) "
        "WHERE scope_id IS NOT NULL"
    )
    conn.execute(
        "CREATE INDEX IF NOT EXISTS idx_llm_mem_tags_gin ON llm_memories USING GIN (tags)"
    )
    conn.execute(
        "CREATE INDEX IF NOT EXISTS idx_llm_mem_created ON llm_memories (created_at DESC)"
    )
    conn.execute(
        "CREATE INDEX IF NOT EXISTS idx_llm_mem_hot "
        "ON llm_memories (use_count DESC, last_used_at DESC) WHERE use_count > 0"
    )
    conn.execute(
        "ALTER TABLE llm_memories SET ("
        "autovacuum_vacuum_scale_factor = 0.1, "
        "autovacuum_analyze_scale_factor = 0.05)"
    )

    # 5. llm_stats_cache — real-time counter cache maintained by trigger.
    conn.execute(
        """
        CREATE TABLE IF NOT EXISTS llm_stats_cache (
            scope TEXT NOT NULL,
            kind TEXT NOT NULL,
            total_count BIGINT NOT NULL DEFAULT 0,
            total_uses BIGINT NOT NULL DEFAULT 0,
            last_updated TIMESTAMPTZ NOT NULL DEFAULT NOW(),
            PRIMARY KEY (scope, kind)
        )
        """
    )
    # Counter maintenance trigger.
    conn.execute(
        """
        CREATE OR REPLACE FUNCTION llm_memories_stats_sync() RETURNS TRIGGER AS $$
        BEGIN
          IF TG_OP = 'INSERT' THEN
            INSERT INTO llm_stats_cache (scope, kind, total_count, total_uses)
            VALUES (NEW.scope, NEW.kind, 1, 1)
            ON CONFLICT (scope, kind) DO UPDATE
              SET total_count = llm_stats_cache.total_count + 1,
                  total_uses = llm_stats_cache.total_uses + 1,
                  last_updated = NOW();
          ELSIF TG_OP = 'DELETE' THEN
            UPDATE llm_stats_cache
            SET total_count = GREATEST(0, total_count - 1),
                total_uses = GREATEST(0, total_uses - COALESCE(OLD.use_count, 1)),
                last_updated = NOW()
            WHERE scope = OLD.scope AND kind = OLD.kind;
          ELSIF TG_OP = 'UPDATE' THEN
            UPDATE llm_stats_cache
            SET total_uses = total_uses + (NEW.use_count - OLD.use_count),
                last_updated = NOW()
            WHERE scope = NEW.scope AND kind = NEW.kind;
          END IF;
          RETURN NULL;
        END;
        $$ LANGUAGE plpgsql
        """
    )
    conn.execute(
        """
        DO $$
        BEGIN
          IF NOT EXISTS (SELECT 1 FROM pg_trigger WHERE tgname = 'trg_llm_memories_stats') THEN
            CREATE TRIGGER trg_llm_memories_stats
              AFTER INSERT OR UPDATE OR DELETE ON llm_memories
              FOR EACH ROW EXECUTE FUNCTION llm_memories_stats_sync();
          END IF;
        END $$
        """
    )


