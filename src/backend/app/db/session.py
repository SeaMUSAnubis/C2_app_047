def _risk_distribution() -> list[dict[str, Any]]:
    buckets = [
        ("0-30", 0, 30, "#22c55e"),
        ("31-60", 31, 60, "#f59e0b"),
        ("61-80", 61, 80, "#fb923c"),
        ("81-100", 81, 100, "#fb1746"),
    ]
    with get_connection() as conn:
        rows = conn.execute(
            """
            SELECT risk_score FROM users
            UNION ALL
            SELECT risk_score FROM devices
            """
        ).fetchall()
    total = max(len(rows), 1)
    result = []
    for label, start, end, color in buckets:
        count = sum(1 for row in rows if start <= int(row["risk_score"] or 0) <= end)
        result.append({"label": label, "value": round(count * 100 / total), "color": color})
    return result

