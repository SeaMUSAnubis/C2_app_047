def update_account(account_id: int, payload: dict[str, Any]) -> dict[str, Any] | None:
    fields: list[str] = []
    params: list[Any] = []
    if "full_name" in payload and payload["full_name"] is not None:
        fields.append("full_name = %s")
        params.append(payload["full_name"])
    if "role" in payload and payload["role"] is not None:
        fields.append("role = %s")
        params.append(payload["role"])
    if "is_active" in payload and payload["is_active"] is not None:
        fields.append("is_active = %s")
        params.append(bool(payload["is_active"]))
    if "password" in payload and payload["password"]:
        fields.append("password_hash = %s")
        params.append(hash_password(payload["password"]))
    if not fields:
        return get_account(account_id)
    params.append(account_id)
    with get_connection() as conn:
        row = conn.execute(
            f"""
            UPDATE app_accounts SET {", ".join(fields)}
            WHERE id = %s
            RETURNING id, email, full_name, role, is_active, created_at
            """,
            params,
        ).fetchone()
        return _row_to_dict(row) or None


