def batch_ingest_raw_logs(records: list[dict[str, Any]]) -> dict[str, Any]:
    created_or_updated = 0
    failed = 0
    errors: list[dict[str, Any]] = []
    now = utc_now()
    with get_connection() as conn:
        for idx, record in enumerate(records):
            conn.execute("SAVEPOINT sp_raw_batch")
            try:
                raw_payload_json = json.dumps(record.get("raw_payload") or {}, sort_keys=True)
                ingest_metadata_json = json.dumps(
                    record.get("ingest_metadata") or {}, sort_keys=True
                )
                conn.execute(
                    """
                    INSERT INTO raw_user_logs (
                        source_id, collector_type, event_type, timestamp,
                        user_id, device_id, raw_payload_json, ingest_metadata_json, created_at
                    )
                    VALUES (%s, %s, %s, %s, %s, %s, %s, %s, %s)
                    ON CONFLICT(source_id) DO UPDATE SET
                        collector_type = excluded.collector_type,
                        event_type = excluded.event_type,
                        timestamp = excluded.timestamp,
                        user_id = excluded.user_id,
                        device_id = excluded.device_id,
                        raw_payload_json = excluded.raw_payload_json,
                        ingest_metadata_json = excluded.ingest_metadata_json
                    """,
                    (
                        record["source_id"],
                        record["collector_type"],
                        record["event_type"],
                        record["timestamp"],
                        record.get("user_id"),
                        record.get("device_id"),
                        raw_payload_json,
                        ingest_metadata_json,
                        now,
                    ),
                )
                conn.execute("RELEASE SAVEPOINT sp_raw_batch")
                created_or_updated += 1
            except Exception:
                logger.exception("Failed to ingest raw log at index %d", idx)
                conn.execute("ROLLBACK TO SAVEPOINT sp_raw_batch")
                conn.execute("RELEASE SAVEPOINT sp_raw_batch")
                failed += 1
                errors.append({"index": idx, "error": "failed_to_ingest_record"})
    return {"created_or_updated": created_or_updated, "failed": failed, "errors": errors}


