def list_alerts_paged(limit: int = 50, offset: int = 0) -> list[dict[str, Any]]:
    sql = """
        SELECT
            a.id, a.user_id, a.device_id, a.event_log_id, a.model_version,
            a.title, a.severity, a.status, a.risk_score, a.anomaly_score,
            a.risk_factors_json, a.explanation, a.detected_at, a.updated_at,
            COALESCE(u.full_name, u.username, a.user_id) AS user_name,
            COALESCE(d.hostname, a.device_id) AS device_name
        FROM alerts a
        LEFT JOIN users u ON u.id = a.user_id
        LEFT JOIN devices d ON d.id = a.device_id
        ORDER BY a.risk_score DESC, a.detected_at DESC, a.id DESC
        LIMIT %s OFFSET %s
    """
    with get_connection() as conn:
        rows = [_row_to_dict(row) or {} for row in conn.execute(sql, (limit, offset)).fetchall()]
    for row in rows:
        raw_factors = row.pop("risk_factors_json", None)
        row["risk_factors"] = json.loads(raw_factors) if raw_factors else []
    return rows


