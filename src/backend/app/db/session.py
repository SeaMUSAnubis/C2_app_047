def list_pending_raw_logs(limit: int = 200) -> list[dict[str, Any]]:
    """Fetch raw_user_logs rows where normalized_event_id IS NULL.

    Ordered by id ASC so older pending rows are processed first (FIFO).
    """
    with get_connection() as conn:
        rows = conn.execute(
            """
            SELECT * FROM raw_user_logs
            WHERE normalized_event_id IS NULL
            ORDER BY id ASC
            LIMIT %s
            """,
            (limit,),
        ).fetchall()
    return [_decode_raw_log_fields(_row_to_dict(row) or {}) for row in rows]


def count_pending_raw_logs() -> int:
    with get_connection() as conn:
        row = conn.execute(
            "SELECT COUNT(*) AS c FROM raw_user_logs WHERE normalized_event_id IS NULL"
        ).fetchone()
        return int(row["c"]) if row else 0


