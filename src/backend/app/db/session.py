def _relative_time(value: Any) -> str:
    parsed = _parse_timestamp(value)
    if not parsed:
        return "Chưa rõ"
    return parsed.strftime("%Y-%m-%d %H:%M")

def _time_label(value: Any) -> str:
    parsed = _parse_timestamp(value)
    if not parsed:
        return "--:--"
    return parsed.strftime("%H:%M")

def _timestamp_iso(value: Any) -> str:
    parsed = _parse_timestamp(value)
    if not parsed:
        return utc_now()
    return parsed.replace(microsecond=0).isoformat()

def _event_detail(row: dict[str, Any]) -> str:
    user = row.get("user_name") or "Người dùng không xác định"
    device = row.get("device_name") or row.get("device_id") or "thiết bị không xác định"
    resource = row.get("resource") or "tài nguyên nội bộ"
    action = row.get("action") or row.get("event_type") or "sự kiện"
    source_ip = row.get("source_ip")
    count = int(row.get("event_count") or 0)
    ip_part = f" · IP {source_ip}" if source_ip else ""
    prefix = f"{count} sự kiện cùng loại. Mới nhất: " if count > 1 else ""
    return f"{prefix}{user} · {device}{ip_part} · {action} trên {resource}."

def _parse_timestamp(value: Any) -> datetime | None:
    if value is None:
        return None
    text = str(value).strip()
    for fmt in (None, "%m/%d/%Y %H:%M:%S", "%Y-%m-%d %H:%M:%S"):
        try:
            if fmt is None:
                return datetime.fromisoformat(text.replace("Z", "+00:00"))
            return datetime.strptime(text, fmt)
        except ValueError:
            continue
    return None

def _event_title(event_type: Any, action: Any) -> str:
    event = str(event_type or "event").lower()
    action_text = str(action or "").lower()
    if event == "logon":
        return "Đăng nhập bất thường" if "logon" in action_text else "Sự kiện đăng nhập"
    if event == "http":
        return "Truy cập web được ghi nhận"
    if event == "email":
        return "Hoạt động email"
    if event == "file":
        return "Truy cập tệp"
    if event == "device":
        return "Hoạt động thiết bị"
    return event.upper()

def _mitre_label(title: str) -> str:
    lowered = title.lower()
    if "logon" in lowered or "đăng nhập" in lowered:
        return "T1078 - Valid Accounts"
    if "http" in lowered or "web" in lowered:
        return "T1071 - Application Layer Protocol"
    if "file" in lowered or "data" in lowered:
        return "T1005 - Data from Local System"
    return "UEBA - Behavioral Anomaly"


