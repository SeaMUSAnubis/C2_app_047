def _risk_trend() -> list[dict[str, Any]]:
    sql = """
        SELECT substring(timestamp from 12 for 2) AS hour, AVG(risk_value) AS value
        FROM (
            SELECT e.timestamp, COALESCE(a.risk_score, u.risk_score, d.risk_score, 0) AS risk_value
            FROM event_logs e
            LEFT JOIN users u ON u.id = e.user_id
            LEFT JOIN devices d ON d.id = e.device_id
            LEFT JOIN alerts a ON a.event_log_id = e.id
            ORDER BY e.timestamp DESC, e.id DESC
            LIMIT 200
        ) recent
        GROUP BY substring(timestamp from 12 for 2)
        ORDER BY substring(timestamp from 12 for 2)
    """
    with get_connection() as conn:
        rows = [_row_to_dict(row) or {} for row in conn.execute(sql).fetchall()]
    points = [
        {"label": f"{row.get('hour') or '00'}:00", "value": max(1, round(float(row.get("value") or 0)))}
        for row in rows
    ]
    return points or [{"label": label, "value": 0} for label in ("00:00", "06:00", "12:00", "18:00")]

