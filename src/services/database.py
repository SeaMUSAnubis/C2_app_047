def _build_filters(filters: dict[str, Any], columns: dict[str, str]) -> tuple[list[str], list[Any]]:
    where: list[str] = []
    params: list[Any] = []
    for key, column in columns.items():
        value = filters.get(key)
        if value is not None:
            where.append(f"{column} = %s")
            params.append(value)
    return where, params


def _user_filters(filters: dict[str, Any]) -> tuple[list[str], list[Any]]:
    where, params = _build_filters(
        filters,
        {
            "department": "department",
            "job_role": "job_role",
            "status": "status",
        },
    )
    search = filters.get("search")
    if search:
        where.append(
            "(lower(username) LIKE %s OR lower(full_name) LIKE %s OR lower(email) LIKE %s)"
        )
        term = f"%{search.lower()}%"
        params.extend([term, term, term])
    return where, params


