def _fetch_all(
    base_sql: str,
    where: list[str],
    params: list[Any],
    order_by: str,
    filters: dict[str, Any],
) -> list[dict[str, Any]]:
    sql = base_sql
    if where:
        sql += " WHERE " + " AND ".join(where)
    sql += f" ORDER BY {order_by} LIMIT %s OFFSET %s"
    params = [*params, filters.get("limit", 50), filters.get("offset", 0)]
    with get_connection() as conn:
        return [_row_to_dict(row) or {} for row in conn.execute(sql, params).fetchall()]


def _count(table: str, where: list[str], params: list[Any]) -> int:
    sql = f"SELECT COUNT(*) AS count FROM {table}"
    if where:
        sql += " WHERE " + " AND ".join(where)
    with get_connection() as conn:
        return int(conn.execute(sql, params).fetchone()["count"])


def _update_by_id(
    table: str, row_id: str, payload: dict[str, Any], *, allowed: set[str]
) -> dict[str, Any] | None:
    updates = {key: value for key, value in payload.items() if key in allowed}
    if not updates:
        return get_user(row_id) if table == "users" else get_device(row_id)
    updates["updated_at"] = utc_now()
    assignments = ", ".join(f"{key} = %s" for key in updates)
    values = [*updates.values(), row_id]
    with get_connection() as conn:
        result = conn.execute(f"UPDATE {table} SET {assignments} WHERE id = %s", values)
        if result.rowcount == 0:
            return None
    return get_user(row_id) if table == "users" else get_device(row_id)


def _row_to_dict(row: dict[str, Any] | None) -> dict[str, Any] | None:
    if row is None:
        return None
    return dict(row)


